def process_record(record: dict) -> dict:
    """
    Runs BM25 over the 10 candidate passages in one record.
    Self-contained — no global state except rank_bm25.
    Safe for multiprocessing.
    """
    import spacy
    from rank_bm25 import BM25Okapi
    import numpy as np

    # Lazy load spaCy per worker — cached after first call in each process
    if not hasattr(process_record, "_nlp"):
        process_record._nlp = spacy.load(
            "en_core_web_sm",
            disable=["parser", "lemmatizer"]  # NER only — faster
        )
    nlp = process_record._nlp

    query       = record["question"]
    titles      = record["context"]["title"]
    sentences   = record["context"]["sentences"]
    gold_titles = set(record["supporting_facts"]["title"])

    # Build 10 passages
    passages = [
        {
            "title":   title,
            "text":    " ".join(sents),
            "is_gold": title in gold_titles,
        }
        for title, sents in zip(titles, sentences)
    ]

    # BM25
    tokenised  = [p["text"].lower().split() for p in passages]
    bm25       = BM25Okapi(tokenised)
    scores     = bm25.get_scores(query.lower().split())
    ranked_idx = scores.argsort()[::-1]
    ranked     = [
        {
            **passages[i],
            "bm25_score": round(float(scores[i]), 4),
            "rank":       int(rank + 1),
        }
        for rank, i in enumerate(ranked_idx)
    ]

    # Metrics
    top1_correct    = ranked[0]["is_gold"]
    first_gold_rank = next((r["rank"] for r in ranked if r["is_gold"]), 11)
    gold_ranks      = [r["rank"] for r in ranked if r["is_gold"]]
    mrr             = round(1.0 / first_gold_rank, 4)

    hits_at_1  = int(any(r["rank"] <= 1  and r["is_gold"] for r in ranked))
    hits_at_3  = int(any(r["rank"] <= 3  and r["is_gold"] for r in ranked))
    hits_at_10 = int(any(r["rank"] <= 10 and r["is_gold"] for r in ranked))

    # nDCG@10
    relevances = [1 if r["is_gold"] else 0 for r in ranked[:10]]
    dcg        = sum(rel / np.log2(i + 2) for i, rel in enumerate(relevances))
    idcg       = sum(1.0 / np.log2(i + 2) for i in range(min(2, 10)))
    ndcg       = round(dcg / idcg, 4) if idcg > 0 else 0.0

    recall_at_10 = round(
        sum(1 for r in ranked[:10] if r["is_gold"]) / max(len(gold_titles), 1),
        4
    )

    # NER for agent context signals
    top1_ents = set(ent.text.lower() for ent in nlp(ranked[0]["text"]).ents)
    gold_text = next((p["text"] for p in passages if p["is_gold"]), "")
    gold_ents = set(ent.text.lower() for ent in nlp(gold_text).ents)
    q_ents    = [ent.text for ent in nlp(query).ents]

    return {
        # Identity
        "id":    record["id"],
        "type":  record["type"],
        "level": record["level"],
        "split": None,          # filled in after map

        # Query
        "query":       query,
        "answer":      record["answer"],
        "gold_titles": list(gold_titles),

        # Ranking
        "ranked_passages": [
            {
                "title":      r["title"],
                "bm25_score": r["bm25_score"],
                "rank":       r["rank"],
                "is_gold":    r["is_gold"],
            }
            for r in ranked
        ],
        "top1_title":      ranked[0]["title"],
        "top1_correct":    top1_correct,
        "first_gold_rank": first_gold_rank,
        "gold_ranks":      gold_ranks,

        # Metrics
        "hits_at_1":    hits_at_1,
        "hits_at_3":    hits_at_3,
        "hits_at_10":   hits_at_10,
        "mrr":          mrr,
        "ndcg_at_10":   ndcg,
        "recall_at_10": recall_at_10,

        # Agent signals
        "only_answer_has":   sorted(gold_ents - top1_ents),
        "only_wrong_has":    sorted(top1_ents - gold_ents),
        "bridge_candidates": q_ents,

        # Full candidate text for agents
        "candidates": [
            {
                "title":      r["title"],
                "text":       r["text"],
                "bm25_score": r["bm25_score"],
                "rank":       r["rank"],
                "is_gold":    r["is_gold"],
            }
            for r in ranked
        ],
    }

def run_bm25_baseline_t4(train_ds, dev_ds):

    all_results = []

    for split_name, split_ds in [("train", train_ds), ("dev", dev_ds)]:
        print(f"\nProcessing {split_name} ({len(split_ds)} records) "
              f"on 4 cores...")
        t0 = time.time()

        # datasets.map handles parallelism, progress bar, and batching
        mapped = split_ds.map(
            process_record,
            num_proc         = 4,       # all T4 CPU cores
            desc             = f"BM25 {split_name}",
            load_from_cache_file  = False,   # always rerun fresh
        )

        # Add split label and convert to list
        results = mapped.to_list()
        for r in results:
            r["split"] = split_name
        all_results.extend(results)

        elapsed = time.time() - t0
        n       = len(split_ds)
        print(f"  {split_name}: {n} records in {elapsed:.1f}s "
              f"({n/elapsed:.0f} rec/s)")

        # Checkpoint after each split
        print(f"  Checkpointing {len(all_results)} results...")
        with open(CHECKPOINT_PATH, "w") as f:
            json.dump(all_results, f)
        print(f"  Checkpoint saved.")

    return all_results

