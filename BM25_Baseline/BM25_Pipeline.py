def print_metrics(records, label):
    n = len(records)
    if n == 0:
        return
    print(f"\n{'='*50}")
    print(f"  {label}  (n={n})")
    print(f"{'='*50}")
    print(f"  Hits@1:        {sum(r['hits_at_1']   for r in records)/n:.4f}")
    print(f"  Hits@3:        {sum(r['hits_at_3']   for r in records)/n:.4f}")
    print(f"  Hits@10:       {sum(r['hits_at_10']  for r in records)/n:.4f}")
    print(f"  MRR:           {sum(r['mrr']          for r in records)/n:.4f}")
    print(f"  nDCG@10:       {sum(r['ndcg_at_10']   for r in records)/n:.4f}")
    print(f"  Recall@10:     {sum(r['recall_at_10'] for r in records)/n:.4f}")
    fails = sum(1 for r in records if not r["top1_correct"])
    print(f"  Hard failures: {fails}  ({100*fails/n:.1f}%)")

