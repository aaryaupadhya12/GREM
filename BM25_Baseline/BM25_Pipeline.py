def extract_passages_from_record(record: dict) -> list[dict]:
    """
    Extract the 10 candidate passages for this question.
    Each passage = all sentences for that title joined.
    """
    passages = []
    titles    = record["context"]["title"]
    sentences = record["context"]["sentences"]
    gold_titles = set(record["supporting_facts"]["title"])

    for title, sents in zip(titles, sentences):
        passages.append({
            "title":    title,
            "text":     " ".join(sents),
            "is_gold":  title in gold_titles,  # True for 2, False for 8
        })

    return passages

